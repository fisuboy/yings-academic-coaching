const spanishContent: Partial<SiteContent> = {};

const chineseContent: Partial<SiteContent> = {};

const localeOverrides: Record<Locale, Partial<SiteContent>> = {
  en: englishContent,
  es: spanishContent,
  zh: chineseContent
};

function mergeContent<T extends Record<string, unknown>>(
  base: T,
  override: Partial<T>
): T {
  const result = { ...base } as T;

  (Object.keys(base) as Array<keyof T>).forEach((key) => {
    const baseValue = base[key];
    const overrideValue = override[key];

    if (overrideValue === undefined || overrideValue === null) {
      result[key] = baseValue;
      return;
    }

    if (Array.isArray(baseValue)) {
      result[key] = (Array.isArray(overrideValue) && overrideValue.length > 0
        ? overrideValue
        : baseValue) as T[keyof T];
      return;
    }

    if (
      typeof baseValue === "object" &&
      baseValue !== null &&
      typeof overrideValue === "object" &&
      overrideValue !== null
    ) {
      result[key] = mergeContent(
        baseValue as Record<string, unknown>,
        overrideValue as Partial<Record<string, unknown>>
      ) as T[keyof T];
      return;
    }

    result[key] = overrideValue as T[keyof T];
  });

  return result;
}

export function getContent(locale: Locale): SiteContent {
  if (locale === "en") {
    return englishContent;
  }

  return mergeContent(englishContent, localeOverrides[locale]);
}
